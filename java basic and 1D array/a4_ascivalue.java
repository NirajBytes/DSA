//****************ABOUT ACII VALUEA************//
public class a4_ascivalue {
    public static void main(String[] args) {
        char ch = 'a';
        int x = ch;
        System.out.print((ch + "\t"));
        System.out.println((x));
        char ch1 = 'A';
        int x1 = ch1;
        System.out.print((ch1 + "\t"));
        System.out.println((x1));
    }
}
