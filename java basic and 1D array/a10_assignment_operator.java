public class a10_assignment_operator {
    public static void main(String[] args) {
        int p=10,q;
        q=p ;
        p+=q;
        System.out.println(p);
        p-=q;
        System.out.println(p);
        p/=q;
        System.out.println(p);
        p*=q;
        System.out.println(p);

    }
}
