public class a3_ques3 {
    public static void main(String[] args) {
        int n=3;
        int count=0;
        for (int i = 0; i < n; i++) {
            for (int j = 0; j < n; j++) {
                System.out.println("hello");
                count++;
                System.out.println(count);
            }
        }
    }
}
// Time complexity O(n^2)